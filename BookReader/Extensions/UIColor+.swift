import Foundation
import UIKit

extension UIColor {
    class func rdDarkYellow() -> UIColor {
        return UIColor(0xeee0ca)
    }
    
    class func rdGreen() -> UIColor {
        return UIColor(0xcdefcd)
    }
    
    class func rdBlue() -> UIColor {
        return UIColor(0xcee9f1)
    }
    
    class func rdLightYellow() -> UIColor {
        return UIColor(0xfbedc7)
    }
    
    class func rdBlack() -> UIColor {
        return UIColor(0x333333)
    }
}
